    public void showDataToComboBox() throws SQLException, ParseException{
        Employee_DAO emp_dao = new Employee_DAO();
        ArrayList<Employee> listEmp = emp_dao.selectAll();
        DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel)boxEmpID.getModel();
        comboBoxModel.removeAllElements();
        for(Employee e: listEmp){
            String emp_ID = e.getID();
            comboBoxModel.addElement(emp_ID);
        }
    }
    public void showDataToTable() throws SQLException, ParseException{
        relative_dao = new Relative_DAO();
        ArrayList<Relative> listRelative = relative_dao.selectAll();
        DefaultTableModel tableModel = (DefaultTableModel)table.getModel();
        tableModel.setRowCount(0);
        for(Relative relative: listRelative){
            String Id = relative.getID();
            String fullname = relative.getFullname();
            String birth = config.f.format(relative.getBirth());
            String email = relative.getEmail();
            String gender = relative.getGender();
            String emp_id = relative.getEmployee().getID();
            String relationship = relative.getRelationship();
            String[] dataRow = {Id, fullname, birth, email, gender, emp_id, relationship};
            tableModel.addRow(dataRow);
        }
    }
    
    public void showDataToTable(String condition) throws SQLException, ParseException{
        relative_dao = new Relative_DAO();
        ArrayList<Relative> listRelative = relative_dao.selectByCondition(condition);
        DefaultTableModel tableModel = (DefaultTableModel)table.getModel();
        tableModel.setRowCount(0);
        for(Relative relative: listRelative){
            String Id = relative.getID();
            String fullname = relative.getFullname();
            String birth = config.f.format(relative.getBirth());
            String email = relative.getEmail();
            String gender = relative.getGender();
            String emp_id = relative.getEmployee().getID();
            String relationship = relative.getRelationship();
            String[] dataRow = {Id, fullname, birth, email, gender, emp_id, relationship};
            tableModel.addRow(dataRow);
        }
    }
