    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String id = txtRelativeID.getText();
        String fullname = txtFullname.getText();
        String textBirth = txtBirth.getText();
        String email = txtEmail.getText();
        String gender = boxGender.getItemAt(boxGender.getSelectedIndex());
        String emp_ID = boxEmpID.getItemAt(boxEmpID.getSelectedIndex());
        String relationship = txtRelationship.getText();
        if(id.equals("") || fullname.equals("") || textBirth.equals("")|| 
           email.equals("")|| relationship.equals("")){
            JOptionPane.showMessageDialog(this, "Yêu cầu nhập đầy đủ thông tin !");
        }
        else{
            try {
                relative_dao = new Relative_DAO();
                Relative r = relative_dao.selectByID(id);
                if(r != null){
                    JOptionPane.showMessageDialog(this, "Thân nhân đã tồn tại!");
                }
                else{
                    Employee_DAO emp_DAO = new Employee_DAO();
                    Employee emp = emp_DAO.selectByID(emp_ID);
                    Date birth = config.f.parse(textBirth);
                    Person relative = new Relative(id, fullname, birth, email, gender, emp, relationship);
                    relative_dao.insert((Relative) relative);
                    JOptionPane.showMessageDialog(this, "Thêm thành công!");
                    reset();
                }
            } 
            catch(ParseException ex){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng ngày tháng năm \n Ví dụ: 10/11/2003");
            }
            catch (SQLException ex) {
                Logger.getLogger(view_employee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

