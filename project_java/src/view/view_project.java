    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        String project_ID = txtProjectID.getText();
        String project_name = txtProjectName.getText();
        String textDateStart = txtDateStart.getText();
        String textDateEnd = txtDateEnd.getText();
        String funding = txtFunding.getText();
        String project_lead = txtProjectLeader.getText();
        if(project_ID.equals("") || project_name.equals("") || textDateStart.equals("")
                || textDateEnd.equals("") || funding.equals("") || project_lead.equals("")){
            JOptionPane.showMessageDialog(this, "Yêu cầu nhập đầy đủ thông tin !");
        }
        else{
            try {
                project_DAO = new Project_DAO();
                Project projectExist = project_DAO.selectByID(project_ID);
                if(projectExist != null){
                    Double kinhPhi = Double.valueOf(funding);
                    Date dateStart = config.f.parse(textDateStart);
                    Date dateEnd = config.f.parse(textDateEnd);
                    Project project = new Project(project_ID, project_name, dateStart, dateEnd, project_lead, kinhPhi);
                    int reply = JOptionPane.showConfirmDialog(null, "Bạn có muốn thay đổi ?", "Yes", JOptionPane.YES_NO_OPTION);
                    if(reply == JOptionPane.YES_OPTION){
                        project_DAO.update(project);
                        reset();
                        JOptionPane.showMessageDialog(this, "Sửa thành công!");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Sửa không thành công!");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Dự án không tồn tại!");
                }
                JDBCConnection.disconnect(project_DAO.getStatement().connect);
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập định dạng ngày tháng là dd/MM/yyyy!");
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số tiền đúng định dạng là số nguyên!");
            } catch (SQLException ex) {
                ex.printStackTrace();
            } 
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

