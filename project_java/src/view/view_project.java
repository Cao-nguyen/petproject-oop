    public void showDataToTable() throws SQLException, ParseException{
        project_DAO = new Project_DAO();
        DefaultTableModel defaultTableModel = (DefaultTableModel)table.getModel();
        defaultTableModel.setRowCount(0);
        ArrayList<Project> listProject = project_DAO.selectAll();
        for(Project p : listProject){
            String project_ID = p.getProject_ID();
            String project_name = p.getProject_name();
            String dateStart = config.f.format(p.getNgayBatDau());
            String dateEnd = config.f.format(p.getNgayKetThuc());
            String project_lead = p.getnguoiChuNhiem();
            BigDecimal bigDecimal = new BigDecimal(p.getKinhPhiDauTu());
            String funding = bigDecimal.toString();
            Object[] dataRow = {project_ID, project_name, dateStart, dateEnd, project_lead, funding};
            defaultTableModel.addRow(dataRow);
        }
        JDBCConnection.disconnect(project_DAO.getStatement().connect);
    }
    
    public void showDateToTable(String condition) throws SQLException, ParseException{
        project_DAO = new Project_DAO();
        DefaultTableModel defaultTableModel = (DefaultTableModel)table.getModel();
        defaultTableModel.setRowCount(0);
        ArrayList<Project> listProject = project_DAO.selectByCondition(condition);
        for(Project p : listProject){
            String project_ID = p.getProject_ID();
            String project_name = p.getProject_name();
            String dateStart = config.f.format(p.getNgayBatDau());
            String dateEnd = config.f.format(p.getNgayKetThuc());
            String project_lead = p.getnguoiChuNhiem();
            BigDecimal bigDecimal = new BigDecimal(p.getKinhPhiDauTu());
            String funding = bigDecimal.toString();
            Object[] dataRow = {project_ID, project_name, dateStart, dateEnd, project_lead, funding};
            defaultTableModel.addRow(dataRow);
        }
        JDBCConnection.disconnect(project_DAO.getStatement().connect);
    }
