    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        ArrayList<Project> listProject;
        try {
            DefaultTableModel tableModel =(DefaultTableModel)table.getModel();
            tableModel.setRowCount(0);
            listProject = project_DAO.selectAll();
            listProject.stream()
                .sorted(Comparator.comparingDouble(Project::getKinhPhiDauTu))
                .forEach(p -> {
                        String project_ID = p.getProject_ID();
                        String project_name = p.getProject_name();
                        String dateStart = config.f.format(p.getNgayBatDau());
                        String dateEnd = config.f.format(p.getNgayKetThuc());
                        String project_lead = p.getnguoiChuNhiem();
                        BigDecimal bigDecimal = new BigDecimal(p.getKinhPhiDauTu());
                        String funding = bigDecimal.toString();
                        Object[] dataRow = {project_ID, project_name, dateStart, dateEnd, project_lead, funding};
                        tableModel.addRow(dataRow);
                 });
        } catch (SQLException | ParseException ex) {
            Logger.getLogger(view_project.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSortActionPerformed

    
