    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String textDate = txtSearchDate.getText();
        String textSearchProjectName = txtSearchProjectName.getText();
        if(textDate.equals("") && textSearchProjectName.equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Ngày bắt đầu dự án hoặc Tên dự án muốn tìm kiếm!");
        }
        else{
            try{
                if(textDate.equals("")){
                    String condition = " project_name = '"+textSearchProjectName+"'";
                    showDateToTable(condition);
                }
                else if(textSearchProjectName.equals("")){
                    Date searchDate = config.f.parse(textDate);
                    String condition = " ngay_bat_dau = '"+config.f.format(searchDate)+"'";
                    showDateToTable(condition);
                }
                else{
                    Date searchDate = config.f.parse(textDate);
                    String condition = " project_name = '"+textSearchProjectName+"' AND ngay_bat_dau = '"+config.f.format(searchDate)+"'";
                    showDateToTable(condition);
                }
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng ngày tháng năm \n Ví dụ: 10/11/2003");
            } catch (SQLException ex) {
                Logger.getLogger(view_project.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
    }//GEN-LAST:event_btnSearchActionPerformed

