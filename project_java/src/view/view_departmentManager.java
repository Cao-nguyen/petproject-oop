    private void btxDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btxDeleteActionPerformed
        String department_ID = boxDepartmentID.getItemAt(boxDepartmentID.getSelectedIndex());
        if(department_ID.equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã phòng ban muốn xóa !");
        }
        else{
            try {
                department_head = new DepartmentManager_DAO();
                DepartmentManager dmExist = department_head.selectByID(department_ID);
                if(dmExist != null){
                    int reply = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa!", "Yes", JOptionPane.YES_NO_OPTION);
                    if(reply == JOptionPane.YES_OPTION){
                        department_head.delete(dmExist);
                        JOptionPane.showMessageDialog(this, "Xóa thành công!");
                        reset();
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Xóa không thành công!");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Phòng ban không tồn tại!");
                }
                JDBCConnection.disconnect(department_head.getStatement().connect);
            } catch (SQLException | ParseException ex) {
                Logger.getLogger(view_department.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btxDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        String department_ID = boxDepartmentID.getItemAt(boxDepartmentID.getSelectedIndex());
        String emp_ID = boxHeadID.getItemAt(boxHeadID.getSelectedIndex());
        String textDate = txtDateStart.getText();
        if(textDate.equals("")){
            JOptionPane.showMessageDialog(this, "Yêu cầu nhập đầy đủ thông tin !");
        }
        else{
            try {
                department_head = new DepartmentManager_DAO();
                DepartmentManager dmExist = department_head.selectByID(department_ID);
                if(dmExist != null){
                    Date dateStart = config.f.parse(textDate);
                    Employee_DAO emp_DAO = new Employee_DAO();
                    Employee emp = emp_DAO.selectByID(emp_ID);
                    Department_DAO department_DAO = new Department_DAO();
                    Department department = department_DAO.selectByID(department_ID);
                    DepartmentManager departmentManager = new DepartmentManager(department, emp, dateStart);
                    int reply = JOptionPane.showConfirmDialog(null, "Bạn có muốn thay đổi trưởng phòng ?", "Yes", JOptionPane.YES_NO_OPTION);
                    if(reply == JOptionPane.YES_OPTION){
                        int result = department_head.update(departmentManager);
                        if(result != 0){
                            reset();
                            JOptionPane.showMessageDialog(this, "Sửa thành công!");
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "Nhân viên này đã quản lý 2 phòng ban!");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Sửa không thành công!");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Phòng ban không tồn tại");
                }
                JDBCConnection.disconnect(department_head.getStatement().connect);
            } catch (SQLException ex) {
                Logger.getLogger(view_employee.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng ngày tháng năm \n Ví dụ: 10/11/2003");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

