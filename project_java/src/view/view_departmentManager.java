    public void showDataToTable() throws SQLException, ParseException{
        department_head = new DepartmentManager_DAO();
        DefaultTableModel defaultTableModel = (DefaultTableModel) table.getModel();
        ArrayList<DepartmentManager> listDepartmentManager = department_head.selectAll();
        defaultTableModel.setRowCount(0);
        for(DepartmentManager departmentManager: listDepartmentManager){
            String head_ID;
            if(departmentManager.getEmployee() == null){
                head_ID = "null";
            }
            else{
                head_ID = departmentManager.getEmployee().getID();
            }
            String department_ID = departmentManager.getDepartment().getDepartment_ID();
            String dateStart = config.f.format(departmentManager.getNgay_bo_nhiem());
            String tbData[] = {department_ID, head_ID, dateStart};
            defaultTableModel.addRow(tbData);
        }
        JDBCConnection.disconnect(department_head.getStatement().connect);
    }
